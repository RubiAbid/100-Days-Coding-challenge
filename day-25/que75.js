/*                 Question 75:
 Compound Assignment Operators: Use compound assignment operators to simplify arithmetic operations.
  Start with x = 4 and perform a series of operations (addition, subtraction, multiplication, division)
  on x using compound operators.*/
function compoundassignment(x) {
    // Adding 6 to x
    x += 6;
    console.log('Addition:', x); // Output: Addition: 10
    // Subtracting 2 from x
    x -= 2;
    console.log('Subtraction:', x); // Output: Subtraction: 8
    // Multiplying x by 4
    x *= 4;
    console.log('Multiplication:', x); // Output: Multiplication: 32
    // Dividing x by 4
    x; //= 4;
    console.log('Division:', x); // Output: Division: 8
}
compoundassignment(4);
