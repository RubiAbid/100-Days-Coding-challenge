/*              Question 83:
Converting to Upper and Lower Case: Create a function that takes
a string, converts it to uppercase, then to lowercase, and logs both.*/
// Function to log the lowercase and uppercase versions of a given string
function cases(str) {
    // Convert the input string to lowercase and log the result
    console.log(str.toLowerCase());
    // Convert the input string to uppercase and log the result
    console.log(str.toUpperCase());
}
// Call the function with the string "tyPeScriPt"
cases("tyPeScriPt");
